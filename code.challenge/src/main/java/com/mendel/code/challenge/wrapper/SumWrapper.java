package com.mendel.code.challenge.wrapper;

public class SumWrapper {
	
	private double sum;
	
	public SumWrapper() {
		super();
	}

	public SumWrapper(double sum) {
		super();
		this.sum = sum;
	}

	public double getSum() {
		return sum;
	}

	public void setSum(double sum) {
		this.sum = sum;
	}

}
